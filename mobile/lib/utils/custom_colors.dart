import 'package:flutter/painting.dart';

class CustomColors {
  CustomColors._();

  // Colors are defined with a hex value as follows:
  // Color(0xAARRGGBB)
  // Where AA is alpha, RR is red, GG is green, and BB is blue
  static const grey =     Color(0xFF4D5057);
  static const green =    Color(0xFF7CA982);
  static const orange =   Color(0xFFC57B57);
  static const purple =   Color(0xFF918EF4);
  static const white =    Color(0xFFFFFFFF);
  static const black =    Color(0xFF000000);
}